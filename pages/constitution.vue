              Cabinet</p>
          </li>
          <li>
            <p>By a joint motion from a majority of members of the Extended
              Cabinet</p>
          </li>
          <li>
            <p>A letter signed by 10 members of the Society and one member of
              the Cabinet delivered to the Secretary, Vice-President, or
              President.</p>
          </li>
        </ol>
      </li>
      <li>
        <p>Upon an impeachment motion being called, the following actions
          must occur for the impeachment to be valid:</p>
        <ol>
          <li>
            <p>There must be a meeting of the Cabinet within 7 days of receipt
              of a valid impeachment motion to set out a timeline for a vote to be
              held on the impeachment motion, this vote must occur within 14 days of
              this meeting and must be held online, with a minimum of 48 hours notice
              and a minimum online voting window of 3 hours</p>
          </li>
          <li>
            <p>The Cabinet must vote on whether to carry the motion within this
              meeting</p>
          </li>
          <li>
            <p>Within this meeting of the Cabinet, the subject of the
              impeachment notice must be given the opportunity to step down silently
              with no notice of impeachment being released to the membership</p>
          </li>
          <li>
            <p>An announcement of the impeachment notice must be released
              following the Cabinet meeting via the official communication
              channels</p>
          </li>
          <li>
            <p>The subject of the impeachment motion must be given opportunity
              to lay out their defence to the membership in the form of ONE official
              communication through the official communication channels</p>
          </li>
          <li>
            <p>In the event of a Cabinet member responding to this official
              communication, the subject of the impeachment motion must be given the
              opportunity to lay out their rebuttal in the form of ONE official
              communication through the official communication channels</p>
          </li>
          <li>
            <p>A simple majority of voters within the impeachment vote must
              agree to impeachment</p>
          </li>
        </ol>
      </li>
      <li>
        <p>Upon the passing of a valid impeachment, a by-election will be
          triggered which must be held within 14 days, following the same voting
          rules as that of the impeachment, with the only exception being the vote
          threshold being set to a plurality for any given candidate.</p>
      </li>
      <li>
        <p>In the event of a Core Cabinet member being impeached, the
          Vice-President will take the impeached person’s role and position until
          a replacement is elected.</p>
      </li>
      <li>
        <p>In the case that the Core Cabinet member impeached is the
          Vice-President, no interim Vice-President will be selected.</p>
      </li>
      <li>
        <p>In the case of the position of Vice-President being vacant, a
          member of the Extended Cabinet will be voted on by the Cabinet to take
          on this interim position.</p>
      </li>
    </ol>
    <h1 id="gm">General Meetings</h1>
    <h2 id="gm--assembly">Assembly</h2>
    <ol>
      <li>
        <p>The Assembly at a General Meeting shall represent the Membership.
          The voice of the Assembly shall thus constitute the ultimate governing
          institution of the Society.</p>
      </li>
      <li>
        <p>All members of the Society shall have the right to be present and
          vote at all General Meetings.</p>
      </li>
      <li>
        <p>The executive shall take reasonable measures to ensure all
          members are aware of general meetings and when they will take place, by
          announcement on at least three of the official communication platforms
          of the society, as well as repeating said announcement at the start and
          conclusion of all activities post announcement.</p>
      </li>
      <li>
        <p>Decisions made by the Assembly shall be binding to the
          Executive.</p>
      </li>
    </ol>
    <h2 id="gm--documentation-amendment">Documentation Amendments</h2>
    <ol>
      <li>
        <p>Amendments to the Constitution can be proposed by any member of
          the Society to the executive for advice and acceptance at least 14 days
          prior to a General Meeting.</p>
      </li>
      <li>
        <p>The proposer must show the executive how the update would not
          cause harm to the society.</p>
      </li>
      <li>
        <p>If upon reflection, the proposed amendment would not endanger the
          society, its membership, its possessions, or its affiliates, the
          executive shall publicly announce the proposed amendment to the
          membership for reflection between 7 and 14 days prior to the General
          Meeting.</p>
      </li>
      <li>
        <p>The proposer should also take reasonable measures to inform the
          membership of their proposed update, as well as show how the update
          would be of benefit to the society, provided it is agreed the update
          would not pose a risk to the society.</p>
      </li>
      <li>
        <p>The executive may refuse a proposal, if its passing would
          endanger the society, its membership, its possessions or its
          affiliates.</p>
      </li>
      <li>
        <p>If an individual/s who proposed an amendment retracts the
          proposal prior to the General Meeting, the membership shall be informed
          immediately.</p>
      </li>
      <li>
        <p>Proposed amendments shall be ratified and enacted only with the
          approval of two-thirds, or higher, of the Assembly.</p>
      </li>
      <li>
        <p>When proposing an amendment, the following shall be included;</p>
        <ol>
          <li>
            <p>Original clause (when available)</p>
          </li>
          <li>
            <p>Affected document</p>
          </li>
          <li>
            <p>Proposed clause</p>
          </li>
          <li>
            <p>Description of the proposed amendment</p>
          </li>
        </ol>
      </li>
    </ol>
    <h2 id="gm--disolution">Dissolution of the Society</h2>
    <ol>
      <li>
        <p>The Society may be dissolved at a General Meeting, provided that
          at least twenty-one (21) days notice of the intention of dissolution has
          been given to the Membership.</p>
      </li>
      <li>
        <p>A qualified majority of at least two-thirds of the highest
          between the Assembly or the average weekly attendance of when sessions
          are in motion is required for the Motion of Dissolution to be
          effective.</p>
      </li>
      <li>
        <p>In the event of the Dissolution of the Society, the remaining
          executive shall follow the guidance of the Student Union.</p>
      </li>
    </ol>
    <h2 id="gm--agm">The Annual General Meeting</h2>
    <ol>
      <li>
        <p>Exactly one AGM shall be held each Academic Year in the Lent
          Term.</p>
      </li>
      <li>
        <p>An AGM may be called by:</p>
        <ol>
          <li>
            <p>the President</p>
          </li>
          <li>
            <p>simple majority of the core cabinet</p>
          </li>
          <li>
            <p>simple majority of the extended cabinet</p>
          </li>
          <li>
            <p>by written request of at least five (5) members of the Society,
              plus one member of the core cabinet or two members of the extended
              cabinet.</p>
          </li>
        </ol>
      </li>
      <li>
        <p>Any request to hold an AGM shall be given to an available member
          of the Cabinet. This request shall then be proposed to the Executive as
          soon as possible, who must then call an election as soon as
          possible.</p>
      </li>
      <li>
        <p>At the AGM, the following shall occur:</p>
        <ol>
          <li>
            <p>Any amendments to the Constitution of the Society shall be
              proposed and either ratified or discarded;</p>
          </li>
          <li>
            <p>The President shall present a report of the activities of the
              Society for the prior year;</p>
          </li>
          <li>
            <p>The Treasurer shall present a statement of accounts for
              ratification by the Assembly;</p>
          </li>
          <li>
            <p>The Executive for the following year shall be elected, following
              the Election Procedure (<a href="#bye-election--election-procedure" data-reference-type="ref"
                data-reference="bye-election--election-procedure">7.1</a>).</p>
          </li>
        </ol>
      </li>
      <li>
        <p>At each AGM, all electable positions shall be deemed to be
          available for the following year.</p>
        <p>In the event a member of the Executive desires to remain in office,
          said member shall declare candidacy, even in the absence of
          opposition.</p>
      </li>
    </ol>
    <h2 id="gm--egm">Extraordinary General Meetings</h2>
    <ol>
      <li>
        <p>An EGM may be called for a number of reasons including, but not
          being limited to, invocation of the Articles of Impeachment or an exodus
          of the incumbent Executive.</p>
      </li>
      <li>
        <p>Once called, an EGM must be held within fourteen (14)
          days.</p>
      </li>
    </ol>
    <h2 id="gm--transfer">The Peaceful Transfer of Power</h2>
    <ol>
      <li>
        <p>Upon completion of election at an AGM, the newly elected take
          upon their title, with the inclusion of ‘elect’. For example, President
          Elect.</p>
      </li>
      <li>
        <p>The current executive shall remain in power until the 5th week of
          the summer term, when the complete transfer of power shall become valid,
          subject to conditions, where it would be more ideal to complete the
          transfer earlier.</p>
      </li>
      <li>
        <p>The current and elected executive should use the time prior to
          the complete transfer, as a handover and training period.</p>
      </li>
      <li>
        <p>Upon completion of an election at a Bye-Election, or a General
          Meeting triggered by impeachment, the newly elect will take upon their
          position and full power the position entails with immediate
          effect.</p>
      </li>
    </ol>

          Treasurers shall jointly issue a Statement of Estimation of the Budget
          of the Society. This Statement shall be presented to the incumbent and
          incoming Executives for joint ratification by simple majority of the
          combined committees. Having been ratified, the Statement shall be
          presented during the Annual Review to the Assembly at the General
          Meeting for the Peaceful Transfer of Power. This Statement shall be

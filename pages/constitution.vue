        majority of the combined committees. Having been ratified, the Statement
        shall be presented during the Annual Review to the Assembly at the
        General Meeting for the Peaceful Transfer of Power. This Statement shall
        be displayed so as to be accessible to all members at any point during
        the following year. Following the conclusion of the relevant Academic
        Year, the Statement shall be archived in accordance with Directive
        2023.03.001.</p>
    </li>

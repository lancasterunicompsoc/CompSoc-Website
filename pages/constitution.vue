    <ol>
      <li>
        <p>Any proceeding which may occur at a Bye-Election may also occur
          at a General Meeting (<a href="#gm" data-reference-type="ref" data-reference="gm">6</a>).</p>
      </li>
    </ol>
    <h2 id="bye-election--election-procedure">Election Procedure</h2>
    <ol>
      <li>
        <p>The execution of all Society Elections shall be the
          responsibility of the Returning officer, who shall be the current
          President, as well as a neutral individual who is not a member of the
          society. In the event that the President declares candidacy for the same
          elections, the Vice President shall instead be the Returning
          Officer.</p>
        <p>In the event that the Vice President also declares candidacy for the
          same elections, a suitably competent and independent member of the
          Cabinet shall be nominated for the role.</p>
      </li>
      <li>
        <p>The Membership shall be informed as to the date of the election,
          available positions, and instruction as to declaration of candidacy at
          least four (4) weeks prior to the election.</p>
      </li>
      <li>
        <p>”Re-Open Nominations (RON)” shall be an option for all positions
          in all elections.</p>
      </li>
      <li>
        <p>Preceding elections shall be speeches from all candidates,
          followed by questions put to the candidates by the Assembly. The maximum
          length of speeches shall be determined in advance by the Returning
          Officer.</p>
      </li>
      <li>
        <p>In the event a candidate is unable to attend, or present, the
          candidate can provide written consent to the Returning Officer to
          authorise an individual to give the speech and answer questions on their
          behalf, provided the individual is not banned. This Individual
          authorised may be The Returning Officer themselves, who will always be
          available for the role.</p>
      </li>
      <li>
        <p>In the event that a candidate is absent, without forewarning the
          Returning Officer, that candidate shall be excluded from the
          election.</p>
      </li>
      <li>
        <p>Each member of the Society shall be entitled to exactly one (1)
          vote per position.</p>
      </li>
      <li>
        <p>All members of the Society shall have the right to cast said vote
          either in-person or by proxy.</p>
      </li>
      <li>
        <p>Votes to be made by proxy shall be delegated to another member of
          the Society who must be present at the election.</p>
      </li>
      <li>
        <p>The Returning Officer shall be jointly responsible for the
          counting of the ballot, along with a neutral individual who is not a
          member of the society.</p>
      </li>
      <li>
        <p>Complaints regarding the elections should be brought before the
          Returning Officer.</p>
      </li>
      <li>
        <p>In the event of unsatisfactory resolution, complaints shall be
          escalated to the Student Union.</p>
      </li>
    </ol>

  <h2 id="executive--impeachment">Impeachment</h2>
  <ol>
    <li>
      <p>Any member of the Executive may be impeached for</p>
      <ol>
        <li>
          <p>negligence of duty;</p>
        </li>
        <li>
          <p>gross incompetence;</p>
        </li>
        <li>
          <p>serious breach of any safety documentation; or</p>
        </li>
        <li>
          <p>posing a grave danger to the Society.</p>
        </li>
      </ol>
    </li>
    <li>
      <p>The member against whom these Articles of Impeachment are invoked
        is hereafter referred to as the Defendant.</p>
    </li>
    <li>
      <p>These Articles of Impeachment may be invoked in any one of the
        following manners:</p>
      <ol>
        <li>
          <p>by a single member of the Upper Cabinet against any member of the
            Executive;</p>
        </li>
        <li>
          <p>by a single member of the Lower Cabinet against any member of the
            Executive, not belonging to the Upper Cabinet;</p>
        </li>
        <li>
          <p>by at least two (2) members of the Lower Cabinet against any
            member of the Upper Cabinet;</p>
        </li>
        <li>
          <p>by a single member of the Executive, not belonging to the
            Cabinet, against a single member of the Executive, not belonging to the
            Cabinet;</p>
        </li>
        <li>
          <p>by at least two (2) members of the Executive, not belonging to
            the Cabinet, against a single member of the Lower Cabinet;</p>
        </li>
        <li>
          <p>by at least three (3) members of the Executive, not belonging to
            the Cabinet, against a single member of the Upper Cabinet;</p>
        </li>
        <li>
          <p>by at least two (2) members of the Membership against a single
            member of the Executive, not belonging to the Cabinet;</p>
        </li>
        <li>
          <p>by at least five (5) members of the Membership against a single
            member of the Lower Cabinet; or</p>
        </li>
        <li>
          <p>by at least ten (10) members of the Membership against a single
            member of the Lower Cabinet.</p>
        </li>
      </ol>
    </li>
    <li>
      <p>Notification the invocation of these Articles of Impeachment
        should be given, in signed and dated writing, to the Executive in the
        following order, with the most preference being given to the first
        option, the least to the last:</p>
      <ol>
        <li>
          <p>to the President, unless either the President is to be impeached
            or the President is invoking the articles;</p>
        </li>
        <li>
          <p>to the Vice-President, unless either the Vice-President is to be
            impeached or the Vice-President is invoking the articles against the
            President;</p>
        </li>
        <li>
          <p>to the Secretary.</p>
        </li>
      </ol>
    </li>
    <li>
      <p>Within seven (7) days of receiving notification of the invocation
        of these Articles of Impeachment, the following must occur:</p>
      <ol>
        <li>
          <p>the party being impeached must be notified as to the
            invocation;</p>
        </li>
        <li>
          <p>the Executive must be notified as to the invocation;</p>
        </li>
        <li>
          <p>the Membership must be notified as to the invocation;
            and</p>
        </li>
        <li>
          <p>an EGM must be called.</p>
        </li>
      </ol>
    </li>
    <li>
      <p>At the called EGM, the following shall occur:</p>
      <ol>
        <li>
          <p>the Impeaching Members shall provide argument as to the reason
            both for impeachment and for removal from office;</p>
        </li>
        <li>
          <p>the Defendant shall provide their defence;</p>
        </li>
        <li>
          <p>a Referendum shall be taken of the gathered Assembly as to the
            guilt of the Defendant;</p>
        </li>
        <li>
          <p>in the event of a simple majority in favour of the Impeaching
            Members, the Defendant shall be removed from office;</p>
        </li>
        <li>
          <p>a second Referendum shall be taken of the gathered Assembly as to
            the continued status of membership of the Society of the
            Defendant;</p>
        </li>
        <li>
          <p>in the event of a super-majority of two-thirds of the gathered
            Assembly in favour, the membership of the Society of the Defendant shall
            be revoked.</p>
        </li>
      </ol>
    </li>
    <li>
      <p>The results of impeachment are subject to appeal only to the
        extent required by Union documentation.</p>
    </li>
    <li>
      <p>In the event of removal from office, said member shall be
        disqualified from holding office within the Executive again.</p>
    </li>
    <li>
      <p>No member may invoke these Articles of Impeachment more than once
        in a term.</p>
    </li>
    <li>
      <p>These Articles of Impeachment may be invoked only whilst the
        Executive is in session.</p>
    </li>
  </ol>
  <h1 id="gm">General Meetings</h1>
  <h2 id="gm--assembly">Assembly</h2>
  <ol>
    <li>
      <p>The Assembly at a General Meeting shall represent the totality of
        the Membership. The voice of the Assembly shall thus constitute ultimate
        governing institution of the Society.</p>
    </li>
    <li>
      <p>All members of the Society shall have the right to be present and
        vote at all General Meetings.</p>
    </li>
    <li>
      <p>Decisions made by the Assembly shall be binding to the
        Executive.</p>
    </li>
  </ol>
  <h2 id="gm--consitutional-amendment">Constitutional Amendments</h2>
  <ol>
    <li>
      <p>Amendments to the substance of the Constitution of the Society
        shall be proposed at a General Meeting.</p>
    </li>
    <li>
      <p>Proposed amendments shall be ratified and enacted only with the
        approval of a super-majority of two-thirds of the Assembly.</p>
    </li>
    <li>
      <p>Prior to referendum, proposed amendments shall be proclaimed to
        the convened Assembly by the President.</p>
    </li>
  </ol>
  <h2 id="gm--disolution">Dissolution of the Society</h2>
  <ol>
    <li>
      <p>The Society may be dissolved at a General Meeting, provided that
        at least twenty-one (21) days notice of the intention of dissolution has
        been given to the Membership.</p>
    </li>
    <li>
      <p>A qualified super-majority of at least two-thirds of the Assembly
        is required for the Motion of Dissolution to be effective.</p>
    </li>
    <li>
      <p>In the event of the Dissolution of the Society, the following
        must occur:</p>
      <ol>
        <li>
          <p>any debts owed by the Society shall be paid in full;</p>
        </li>
        <li>
          <p>any financial assets shall be distributed as required by Union
            and University policy; and</p>
        </li>
        <li>
          <p>the assets of the Society shall be distributed to applicable
            societies.</p>
        </li>
      </ol>
    </li>
  </ol>
  <h2 id="gm--agm">The Annual General Meeting</h2>
  <ol>
    <li>
      <p>Exactly one AGM shall be held each Academic Year in the Lent
        Term.</p>
    </li>
    <li>
      <p>An AGM may be called by be called by the following persons:</p>
      <ol>
        <li>
          <p>by the President;</p>
        </li>
        <li>
          <p>by a simple majority of the Upper Cabinet;</p>
        </li>
        <li>
          <p>by a simple majority of the Lower Cabinet; or</p>
        </li>
        <li>
          <p>by written request of at least five (5) members of the
            Society.</p>
        </li>
      </ol>
    </li>
    <li>
      <p>Any request to hold an AGM shall be given, in signed and dated
        writing, to the Secretary. This request shall then be proposed to the
        Executive at the next convention of the same.</p>
    </li>
    <li>
      <p>Reasonable measures shall be undertaken by the incumbent
        Executive to ensure the timely execution of the request and the
        viability of participation of all interested members.</p>
    </li>
    <li>
      <p>At the AGM, the following shall occur:</p>
      <ol>
        <li>
          <p>any amendments to the Constitution or to the Bye-Laws of the
            Society shall be proposed and ratified;</p>
        </li>
        <li>
          <p>the President shall present a report of the activities of the
            Society for the prior year;</p>
        </li>
        <li>
          <p>the Treasurer shall present a statement of accounts for
            ratification by the Assembly;</p>
        </li>
        <li>
          <p>the Commissioner for Continuity shall announce the Commissioner
            for the following year, in the absence of a Commissioner, this role
            shall be filled by the Vice-President; and</p>
        </li>
        <li>
          <p>the Executive for the following year shall be elected, following
            the Election Procedure (<a href="#bye-election--election-procedure" data-reference-type="ref"
              data-reference="bye-election--election-procedure">7.1</a>).</p>
        </li>
      </ol>
    </li>
    <li>
      <p>At each AGM, all electable positions shall be deemed to be
        available for the following year. In the event a member of the Executive
        desires to remain in office, said member shall declare candidacy, even
        in the absence of opposition.</p>
    </li>
  </ol>

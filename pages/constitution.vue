    </h1>
    <h2 id="gm--assembly">
      Assembly
    </h2>
    <ol>
      <li>
        <p>
          The Assembly at a General Meeting shall represent the totality of the
          Membership. The voice of the Assembly shall thus constitute ultimate
          governing institution of the Society.
        </p>
      </li>
      <li>
        <p>
          All members of the Society shall have the right to be present and vote
          at all General Meetings.
        </p>
      </li>
      <li>
        <p>Decisions made by the Assembly shall be binding to the Executive.</p>
      </li>
    </ol>
    <h2 id="gm--consitutional-amendment">
      Constitutional Amendments
    </h2>
    <ol>
      <li>
        <p>
          Amendments to the substance of the Constitution of the Society shall
          be proposed at a General Meeting.
        </p>
      </li>
      <li>
        <p>
          Proposed amendments shall be ratified and enacted only with the
          approval of a super-majority of two-thirds of the Assembly.
        </p>
      </li>
      <li>
        <p>
          Prior to referendum, proposed amendments shall be proclaimed to the
          convened Assembly by the President.
        </p>
      </li>
    </ol>
    <h2 id="gm--disolution">
      Dissolution of the Society
    </h2>
    <ol>
      <li>
        <p>
          The Society may be dissolved at a General Meeting, provided that at
          least twenty-one (21) days notice of the intention of dissolution has
          been given to the Membership.
        </p>
      </li>
      <li>
        <p>
          A qualified super-majority of at least two-thirds of the Assembly is
          required for the Motion of Dissolution to be effective.
        </p>
      </li>
      <li>
        <p>
          In the event of the Dissolution of the Society, the following must
          occur:
        </p>
        <ol>
          <li>
            <p>any debts owed by the Society shall be paid in full;</p>
          </li>
          <li>
            <p>
              any financial assets shall be distributed as required by Union and
              University policy; and
            </p>
          </li>
          <li>
            <p>
              the assets of the Society shall be distributed to applicable
              societies.
            </p>
          </li>
        </ol>
      </li>
    </ol>

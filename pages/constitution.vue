    <h2 id="executive--impeachment">
      Impeachment
    </h2>
    <ol>
      <li>
        <p>Any member of the Executive may be impeached for</p>
        <ol>
          <li>
            <p>negligence of duty;</p>
          </li>
          <li>
            <p>gross incompetence;</p>
          </li>
          <li>
            <p>serious breach of any safety documentation; or</p>
          </li>
          <li>
            <p>posing a grave danger to the Society.</p>
          </li>
        </ol>
      </li>
      <li>
        <p>
          The member against whom these Articles of Impeachment are invoked is
          hereafter referred to as the Defendant.
        </p>
      </li>
      <li>
        <p>
          These Articles of Impeachment may be invoked in any one of the
          following manners:
        </p>
        <ol>
          <li>
            <p>
              by a single member of the Upper Cabinet against any member of the
              Executive;
            </p>
          </li>
          <li>
            <p>
              by a single member of the Lower Cabinet against any member of the
              Executive, not belonging to the Upper Cabinet;
            </p>
          </li>
          <li>
            <p>
              by at least two (2) members of the Lower Cabinet against any
              member of the Upper Cabinet;
            </p>
          </li>
          <li>
            <p>
              by a single member of the Executive, not belonging to the Cabinet,
              against a single member of the Executive, not belonging to the
              Cabinet;
            </p>
          </li>
          <li>
            <p>
              by at least two (2) members of the Executive, not belonging to the
              Cabinet, against a single member of the Lower Cabinet;
            </p>
          </li>
          <li>
            <p>
              by at least three (3) members of the Executive, not belonging to
              the Cabinet, against a single member of the Upper Cabinet;
            </p>
          </li>
          <li>
            <p>
              by at least two (2) members of the Membership against a single
              member of the Executive, not belonging to the Cabinet;
            </p>
          </li>
          <li>
            <p>
              by at least five (5) members of the Membership against a single
              member of the Lower Cabinet; or
            </p>
          </li>
          <li>
            <p>
              by at least ten (10) members of the Membership against a single
              member of the Lower Cabinet.
            </p>
          </li>
        </ol>
      </li>
      <li>
        <p>
          Notification the invocation of these Articles of Impeachment should be
          given, in signed and dated writing, to the Executive in the following
          order, with the most preference being given to the first option, the
          least to the last:
        </p>
        <ol>
          <li>
            <p>
              to the President, unless either the President is to be impeached
              or the President is invoking the articles;
            </p>
          </li>
          <li>
            <p>
              to the Vice-President, unless either the Vice-President is to be
              impeached or the Vice-President is invoking the articles against
              the President;
            </p>
          </li>
          <li>
            <p>to the Secretary.</p>
          </li>
        </ol>
      </li>
      <li>
        <p>
          Within seven (7) days of receiving notification of the invocation of
          these Articles of Impeachment, the following must occur:
        </p>
        <ol>
          <li>
            <p>
              the party being impeached must be notified as to the invocation;
            </p>
          </li>
          <li>
            <p>the Executive must be notified as to the invocation;</p>
          </li>
          <li>
            <p>the Membership must be notified as to the invocation; and</p>
          </li>
          <li>
            <p>an EGM must be called.</p>
          </li>
        </ol>
      </li>
      <li>
        <p>At the called EGM, the following shall occur:</p>
        <ol>
          <li>
            <p>
              the Impeaching Members shall provide argument as to the reason
              both for impeachment and for removal from office;
            </p>
          </li>
          <li>
            <p>the Defendant shall provide their defence;</p>
          </li>
          <li>
            <p>
              a Referendum shall be taken of the gathered Assembly as to the
              guilt of the Defendant;
            </p>
          </li>
          <li>
            <p>
              in the event of a simple majority in favour of the Impeaching
              Members, the Defendant shall be removed from office;
            </p>
          </li>
          <li>
            <p>
              a second Referendum shall be taken of the gathered Assembly as to
              the continued status of membership of the Society of the
              Defendant;
            </p>
          </li>
          <li>
            <p>
              in the event of a super-majority of two-thirds of the gathered
              Assembly in favour, the membership of the Society of the Defendant
              shall be revoked.
            </p>
          </li>
        </ol>
      </li>
      <li>
        <p>
          The results of impeachment are subject to appeal only to the extent
          required by Union documentation.
        </p>
      </li>
      <li>
        <p>
          In the event of removal from office, said member shall be disqualified
          from holding office within the Executive again.
        </p>
      </li>
      <li>
        <p>
          No member may invoke these Articles of Impeachment more than once in a
          term.
        </p>
      </li>
      <li>
        <p>
          These Articles of Impeachment may be invoked only whilst the Executive
          is in session.
        </p>
      </li>
    </ol>

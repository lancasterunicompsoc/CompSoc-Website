    <h2 id="bye-election--election-procedure">
      Election Procedure
    </h2>
    <ol>
      <li>
        <p>
          The execution of all Society Elections shall be the responsibility of
          the President, who shall act as Returning Officer. In the event that
          the President declares candidacy for the same elections, the
          Commissioner for Continuity shall instead take this role. In the event
          that the Commissioner for Continuity also declares candidacy for the
          same elections, a suitably competent and independent member of the
          Cabinet shall be nominated for the role.
        </p>
      </li>
      <li>
        <p>
          The Membership shall be informed as to the date of the election,
          available positions, and instruction as to declaration of candidacy at
          least two weeks prior to the election.
        </p>
      </li>
      <li>
        <p>
          "Re-Open Nominations (RON)" shall be a candidate for all positions in
          all elections.
        </p>
      </li>
      <li>
        <p>
          Preceding elections shall be speeches from all candidates. The maximum
          length of speeches shall be determined in advance by the Returning
          Officer.
        </p>
      </li>
      <li>
        <p>
          Antecedent to speeches shall be questions put to the candidates by the
          Assembly.
        </p>
      </li>
      <li>
        <p>
          In the event a candidate is unable to attend due to means beyond the
          control of the same, written confirmation of intention to stand shall
          be given to the Returning Officer. The confirmation of intention to
          stand may include a speech to be proclaimed to the Assembly by the
          Returning Officer. In the event that no such confirmation is received,
          absent candidates shall be excluded from election.
        </p>
      </li>
      <li>
        <p>
          Each member of the Society shall be entitled to exactly one (1) vote
          per position. All members of the Society shall have the right to cast
          said vote either in person or by proxy. Votes cast by proxy may be
          cast either electronically or by delegation to another member of the
          Society in signed and dated writing.
        </p>
      </li>
      <li>
        <p>
          For each candidate shall a ballot be taken, and the candidate with the
          greatest number of votes shall duly be elected to the office. The
          Returning Officer shall be responsible for the counting of the ballot.
        </p>
      </li>
      <li>
        <p>
          Any and all complaints pertaining to elections shall be brought before
          the Returning Officer. In the event of unsatisfactory resolution,
          complaints shall be escalated to the Chair of the Activities Council
          of the Union, in compliance with Bye-Law 2023.03.002.
        </p>
      </li>
    </ol>
